PROCESSOR 16F887
#include <xc.inc>

CONFIG FOSC = INTRC_NOCLKOUT
CONFIG WDTE = OFF
CONFIG PWRTE = ON
CONFIG MCLRE = OFF
CONFIG CP = OFF
CONFIG CPD = OFF
CONFIG BOREN = OFF
CONFIG IESO = OFF
CONFIG FCMEN = ON
CONFIG DEBUG = ON
CONFIG BOR4V=BOR40V
CONFIG WRT = OFF
PSECT udata
mask:
    DS 1
cont:
    ds 1
PSECT resetVec,class=CODE,delta=2
resetVec:
PAGESEL main
goto main
PSECT code
main:
BANKSEL ANSELH
CLRF ANSELH
BANKSEL TRISB
MOVLW 0b00000000
MOVWF TRISB
BANKSEL PORTB
CLRF PORTB
BANKSEL OSCCON
MOVLW  0b01110000
MOVWF  OSCCON
INICIO:
bcf STATUS,5
bcf STATUS,6
bcf STATUS,0
MOVLW  00000001B
MOVWF  PORTB
GOTO IZQUIERDA
IZQUIERDA:
RLF PORTB
BTFSS PORTB,7
GOTO IZQUIERDA
GOTO DERECHA 
DERECHA:
RRF PORTB
BTFSS PORTB,0
GOTO DERECHA
GOTO IZQUIERDA
END
